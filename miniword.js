// Function to handle color change
function changeColor(color) {
  // Display the selected color value
  document.getElementById('sampleText').style.color = color;
}

// Function to handle font family change
function changeFontFamily(fontFamily) {
  document.getElementById('sampleText').style.fontFamily = fontFamily;
}

// Function to handle font size change
function changeFontSize(fontSize) {
  document.getElementById('sampleText').style.fontSize = fontSize + "px";
}